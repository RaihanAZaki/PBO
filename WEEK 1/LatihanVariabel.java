public class LatihanVariabel {
    public static void main(String[] args) {
        //deklarasi variabel x dan y
        int x = 100;
        int y = 200;
        int total = x + y;

        //menampilkan nilai x dan y
        System.out.println("Nilai x = " + x + "Nilai y = " +y);
        System.out.println ("Nilai x + Nilai y = " + total);
        
        /* 
        Perhitungan 
        Nilai x + y = total
        */
        System.out.println (" Nilai dari " + x + " + " + y + " = " + total);
    }
}